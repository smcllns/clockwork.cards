export interface SpherePosition {
  x: number;
  y: number;
  z: number;
}

export function textToSpherePositions(text: string, scale: number = 1): SpherePosition[] {
  const positions: SpherePosition[] = [];
  const lines = text.split('/');

  let currentY = 0;

  lines.forEach((line, lineIndex) => {
    const chars = line.trim().toUpperCase().split('');
    let currentX = 0;

    chars.forEach((char, charIndex) => {
      const glyph = FONT_5X7[char];
      if (!glyph) return;

      // Convert glyph to positions
      glyph.forEach((row, rowIndex) => {
        row.forEach((pixel, colIndex) => {
          if (pixel === 1) {
            positions.push({
              x: (currentX + colIndex) * scale,
              y: currentY - rowIndex * scale,
              z: 0,
            });
          }
        });
      });

      // Move to next character position (5 pixels wide + 1 spacing)
      currentX += 6;
    });

    // Move to next line (7 pixels tall + 2 spacing)
    currentY -= 9 * scale;
  });

  // Center positions
  if (positions.length > 0) {
    const centerX = positions.reduce((sum, p) => sum + p.x, 0) / positions.length;
    const centerY = positions.reduce((sum, p) => sum + p.y, 0) / positions.length;

    positions.forEach((p) => {
      p.x -= centerX;
      p.y -= centerY;
    });
  }

  return positions;
}
